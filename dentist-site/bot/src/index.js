// ── ОТЗЫВЫ — сохраняем текст ──────────────────────
bot.on('text', (ctx) => {
  const text = ctx.message.text;
  const name = ctx.from.first_name || 'Пациент';

  // Если это не команда меню — считаем отзывом
  const menuItems = ['⭐ Оставить отзыв', '📋 Памятка после лечения', '📲 Перейти в канал',
    '🦷 После пломбы', '🔩 После имплантации', '✂️ После удаления', '👑 После коронки', '← Назад'];

  if (!menuItems.includes(text)) {
    saveContact(ctx);
    // Пересылаем отзыв врачу
    if (process.env.DOCTOR_CHAT_ID) {
      bot.telegram.sendMessage(
        process.env.DOCTOR_CHAT_ID,
        `*Новый отзыв от ${name}*\n\n${text}\n\nTG: @${ctx.from.username || 'нет'}`,
        { parse_mode: 'Markdown' }
      );
    }
    ctx.reply(
      'Спасибо! Отзыв сохранён.\n\nЕсли хотите — подпишитесь на канал. Там разбираю случаи из практики.',
      Markup.inlineKeyboard([
        Markup.button.url('Открыть канал', CHANNEL_URL)
      ])
    );
  }
});

