require('dotenv').config();
const { Telegraf, Markup } = require('telegraf');

const bot = new Telegraf(process.env.BOT_TOKEN);

// Ссылка на TG-канал врача
const CHANNEL_URL = process.env.CHANNEL_URL || 'https://t.me/Artur_Kompanets11';

// ── СТАРТ ──────────────────────────────────────────
bot.start((ctx) => {
  const param = ctx.startPayload; // rec | review | пусто

  if (param === 'rec') {
    return ctx.reply(
      'Выберите процедуру — пришлю памятку:',
      Markup.keyboard([
        ['🦷 После пломбы',     '🔩 После имплантации'],
        ['✂️ После удаления',   '👑 После коронки'],
      ]).resize()
    );
  }

  if (param === 'review') {
    return ctx.reply(
      'Расскажите как прошло лечение — пару предложений своими словами.\n\nЭто поможет другим пациентам принять решение.',
      Markup.removeKeyboard()
    );
  }

  // Стандартный старт
  ctx.reply(
    `Привет! Я бот Артура Компанца.\n\nЧем могу помочь?`,
    Markup.keyboard([
      ['⭐ Оставить отзыв'],
      ['📋 Памятка после лечения'],
      ['📲 Перейти в канал'],
    ]).resize()
  );
});

// ── МЕНЮ ──────────────────────────────────────────
